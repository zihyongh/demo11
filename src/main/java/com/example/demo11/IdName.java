package com.example.demo11;

public class IdName {
	
	private String id;
	
	private String name;

	public IdName() {
		super();
	}

	public IdName(String id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}
	
	
}
